public class belajarswitch {
    
    public static void main(String[] args) {
        int a = 10;

        switch(a) {
            case 10:
            System.out.println("= 10");
            break;
            case 5:
            System.out.println("= 5");
            case -10:
            System.out.println("=-10");
            default :
            System.out.println("none of the above");

            // kalo bisa yang default itu paling bawah biar rapih
            // kalo angka usahain angkanya berurutan
            // switch itu akan ngeprint dimulai dari expression yang ada di switch dan dibawahnya
            // untuk ngeberhentiin nya pake break

        }
        
        }

    }
